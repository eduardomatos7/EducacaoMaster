                construção de respeito pelo corpo e sexualidade próprios e dos outros, e inclui lições sobre consenso,
                DSTs e prazer. A taxa do país de gravidez na adolescência no país está entre as mais baixas do mundo.
                Outros países que também adotam educação sexual compulsória nas escolas são Bélgica, Nova Zelândia,
                Inglaterra e Escócia.<br><br>
            
