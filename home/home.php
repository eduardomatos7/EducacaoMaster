            ou não. Em países como Holanda e Canadá, a Educação Sexual começa ainda no jardim de infância e não faz nenhuma referência explícita ao sexo.
            
            A Holanda entende a sexualidade como algo completamente natural e saudável, e aplicação de programas
                de educação sexual é compulsória em todo o país. O tema é tratado desde os quatro anos de idade, porém
