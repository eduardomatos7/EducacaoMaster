           <h2>O que são IST's, como se propagam e quais as formas de se previnir?</h2>

           <p>As <strong>Infecções Sexualmente Transmissíveis</strong> são causadas por vírus, bactérias ou outros microrganismos.

                Elas são transmitidas, principalmente, por meio do contato sexual sem o uso de camisinha masculina ou
                feminina, com uma pessoa que esteja infectada.
                A transmissão de uma IST também pode ocorrer por meio da transmissão perpendicular para a criança
                durante a gestação, o parto ou a amamentação, quando medidas de prevenção não são realizadas.

                De maneira menos comum, as IST também podem ser transmitidas por meio não sexual, pelo contato de
                mucosas ou pele não íntegra com secreções corporais contaminadas.
                O tratamento das pessoas com IST melhora a qualidade de vida e interrompe a cadeia de transmissão dessas
                infecções.

                O atendimento, o diagnóstico e o tratamento são gratuitos nos serviços de saúde do SUS.

                A terminologia Infecções Sexualmente Transmissíveis passou a ser adotada em substituição à expressão
                Doenças Sexualmente Transmissíveis porque destaca a possibilidade de uma pessoa ter e transmitir uma
                infecção, mesmo não apresentando sinais e sintomas.
            Dessa forma, é de extrema importância que o uso de preservativos seja implementado em qualquer 
            relação sexual, seja ela de forma oral ou não. Usando da forma correta, o uso desses preservativos terão uma eficácia de 98%, 
        não apenas contra a contração de doenças, mas ajuda também na prevenção de gravidez</p><br>

        <h2> Gravidez indesejada </h2>
        <p>A gravidez indesejada é uma gravidez não planejada por um casal ou mulher. A gravidez indesejada pode
                ter muitas causas. Os baixos níveis de educação e a falta de metas governamentais de saúde reprodutiva
                são as principais razões. No Brasil, 8 em cada 10 mulheres que não querem engravidar estão usando algum
                método anticoncepcional. Pesquisas mostram que mais de 55% das mulheres não planejam ter sua última
                gravidez. Os grupos onde isso mais ocorre são adolescentes, usuários de drogas e pessoas com doenças
                crônicas. Isso está associado à minoria de mulheres que usam métodos altamente eficazes, como DIU ou
                implantes hormonais, que não dependem da memória do usuário. Esses dados reforçam a necessidade de
                estratégias diferenciadas para essas populações. O momento da gravidez está relacionado aos direitos
                sexuais fundamentais que todas as mulheres devem desfrutar. A gravidez não planejada traz uma série de mudanças: mudanças físicas; mudanças na vida familiar e
                cotidiana; aumento da assistência à saúde, principalmente porque o pré-natal é fundamental para proteger
                a vida das mães e dos bebês. Além disso, a falta de planejamento da gravidez pode levar a condições mais
                graves como: Interferir no vínculo com o bebê; Interferir nas decisões sobre amamentação; Aumento das
                chances de depressão pós-parto nas mulheres. Adolescente Gravidez Indesejada Adolescentes apresentam
                maior risco de gravidez indesejada devido a fatores socioeconômicos, que dificultam a obtenção de
                anticoncepcionais e informações sobre sexualidade. Além disso, a adolescência é uma época de desafios e
                novas descobertas. É comum os adolescentes pensarem que qualquer coisa pode acontecer com alguém que não
                seja eles.
                Ter um filho nesta fase da vida pode trazer alguns prejuízos ao corpo da mãe, que pode não estar
                completamente formado, ou seja, não está pronto para gerar um bebê. Além das mudanças físicas e psicológicas, a gravidez na adolescência é a causa de muitas desistências.
                Segundo o Fundo de População das Nações Unidas, cerca de 40% das mães adolescentes abandonam a escola,
                veja os principais motivos: sintomas típicos da gravidez; estigma; preconceito; pressão da família,
                escola e amigos. Por outro lado, os casos de gravidez na adolescência caíram cerca de 17%, segundo o
                Ministério da Saúde. A queda foi associada a programas de saúde ampliados, maior acesso a métodos
                contraceptivos e educação sexual escolar. A falta de planejamento faz com que a maioria dessas mulheres
